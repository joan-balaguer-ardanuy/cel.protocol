public interface Ase<E> extends Animal<E> {
	
	/**
	 * Obté, però no allibera, el cap d'aquest {@link Ase}.
	 * @return el cap d'aquest {@link Ase}
	 */
	default E escoltar() {
		return obtenirPare().obtenirEntrada();
	}
	
	/**
	 * Obté i allibera el cap d'aquest {@link Ase}.
	 * @return el cap d'aquest {@link Ase}
	 */
	default E recuperar() {
		Animal<E> animal = obtenirPare();
		animal.alliberar();
		return animal.obtenirEntrada();
	}
}
