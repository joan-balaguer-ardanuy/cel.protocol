public interface Camell<E> extends Animal<E> {

	/**
	 * Estableix l'element especificat en aquest {@link Animal} sense que hi sigui present.
	 * Més formalment, estableix l'especificat element {@code e} a aquest {@link Animal} si
	 * aquest {@link Animal} no conté l'element {@code e} tal que {@code e == e1}.
	 * 
	 * Si aquest {@link Regne} {@link Animal} ha estat modificat el mètode retornarà {@code cert},
	 * {@code fals} del contrari.
	 * 
	 * @param e l'element per ser afegit a aquest {@link Regne} {@link Animal}
	 * @return {@code cert} si aquest {@link Regne} {@link Animal} ha estat modificat, {@code fals}
	 * del contrari
	 */
	boolean establir(E e);
}
